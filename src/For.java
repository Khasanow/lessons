public class For {
    public static void main(String[] args){
        System.out.println("i");
        for(int i=0;i<=10;i=i+1){ /*   i++ == i=i=1  */
            System.out.println("helllloooo "+i);
        }
        System.out.println("j");
        for(int j=0;j<=10;j=j+5){
            System.out.println("helllooo "+j);
        }
        System.out.println("q");
        for(int q=25;q>=10;q=q-5){
            System.out.println("helloo "+q);
        }
    }
}
